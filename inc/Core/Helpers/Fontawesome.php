		 *
		 * @since 1.0.0
		 */
		public static function get_dropdown_options() {
			$lists = self::get_all();

			$options = array_map(
				function ( $value, $font_list ) {
					return array(
						'label' => $font_list,
						'value' => $value,
					);
				},
				array_keys( $lists ),
				array_values( $lists )
			);
			return $options;
		}

